/**
 * Today's date as YYYY-MM-DD in the user's **local** timezone. Use this for the
 * default log date so an engineer logging at 1 AM local time gets the local
 * calendar day, not the UTC one (which can be the day before).
 */
export function localTodayIso(): string {
  const now = new Date();
  const local = new Date(now.getTime() - now.getTimezoneOffset() * 60_000);
  return local.toISOString().slice(0, 10);
}
