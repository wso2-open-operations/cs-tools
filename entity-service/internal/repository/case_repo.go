// CaseRepository defines the persistence operations for the cases table.
type CaseRepository interface {
	// CreateCase inserts a new case row. number and internal_id are generated from
	// DB sequences; id is generated by the DB default.
	CreateCase(ctx context.Context, req domain.CreateCaseRequest) (domain.Case, error)
	// GetCaseByID returns the enriched case view for the given UUID, or a
	// NotFoundError if no matching row exists.
	GetCaseByID(ctx context.Context, id string) (domain.CaseView, error)
	// SearchCases returns a filtered, paginated slice of enriched case views
	// together with the total count of matching rows before pagination.
	// COUNT and SELECT are executed concurrently on separate pool connections.
	SearchCases(ctx context.Context, req domain.SearchCasesRequest) ([]domain.SearchCaseView, int, error)
	// CreateCaseComment inserts a new comment row for the given case.
	CreateCaseComment(ctx context.Context, req domain.CreateCaseCommentRequest) (domain.CaseComment, error)
	// SearchCaseComments returns a paginated slice of comments for the given case
	// together with the total count of matching rows before pagination.
	SearchCaseComments(ctx context.Context, req domain.SearchCaseCommentsRequest) ([]domain.CaseComment, int, error)
	// UpdateCase updates the state and/or priority of the case identified by req.ID.
	// closed_at is set to NOW() when transitioning to closed.
	// Returns a NotFoundError if no matching row exists.
	UpdateCase(ctx context.Context, req domain.UpdateCaseRequest) (domain.Case, error)
}

type caseRepo struct {
	db *pgxpool.Pool
}

// NewCaseRepository constructs a CaseRepository backed by the given connection pool.
func NewCaseRepository(db *pgxpool.Pool) CaseRepository {
	return &caseRepo{db: db}
}

// CreateCase implements CaseRepository.
func (r *caseRepo) CreateCase(ctx context.Context, req domain.CreateCaseRequest) (domain.Case, error) {
	const query = `
		INSERT INTO cases (
			created_by, project_id, deployment_id, deployed_product_id,
			type, subject, description, severity, issue_type, state
		)
		VALUES (
			$1, $2, $3, $4,
			$5::case_type_enum, $6, $7,
			$8::case_severity_enum, $9::case_issue_type_enum,
			'open'::case_state_enum
		)
		RETURNING id, number, internal_id, created_by, project_id, deployment_id, deployed_product_id,
		          subject, description, severity, issue_type, state, created_at, updated_at, closed_at`

	var c domain.Case
	err := r.db.QueryRow(ctx, query,
		req.CreatedBy, req.ProjectID, req.DeploymentID, req.DeployedProductID,
		req.TypeKey, req.Subject, req.Description, string(req.SeverityKey), string(req.IssueTypeKey),
	).Scan(
		&c.ID, &c.Number, &c.InternalID, &c.CreatedBy,
		&c.ProjectID, &c.DeploymentID, &c.DeployedProductID,
		&c.Subject, &c.Description, &c.Severity, &c.IssueType, &c.State,
		&c.CreatedOn, &c.UpdatedOn, &c.ClosedOn,
	)
	if err != nil {
		if pgErr := (*pgconn.PgError)(nil); errors.As(err, &pgErr) {
			switch pgErr.Code {
			case "23503": // foreign_key_violation — one of the referenced IDs does not exist
				return domain.Case{}, &apierror.ValidationError{Msg: "one or more referenced IDs do not exist: " + pgErr.Detail}
			case "P0001": // raise_exception from integrity triggers (deployment/project, deployed_product/deployment, catastrophic priority)
				return domain.Case{}, &apierror.ValidationError{Msg: pgErr.Message}
			}
		}
		return domain.Case{}, fmt.Errorf("create case: %w", err)
	}
	return c, nil
}

// GetCaseByID implements CaseRepository.
func (r *caseRepo) GetCaseByID(ctx context.Context, id string) (domain.CaseView, error) {
	var cv domain.CaseView
	var (
		aeID, aeName                        *string
		pcID, pcNum                         *string
		rcID, rcNum                         *string
		accountID, accountName, accountTier string
		workState                           *string
	)
	err := r.db.QueryRow(ctx,
		`SELECT c.id, c.number, c.internal_id,
		        c.subject, c.description, c.severity, c.issue_type, c.state, c.work_state,
		        c.created_at, c.updated_at, c.closed_at,
		        u.id, u.first_name || ' ' || u.last_name, u.user_name, u.email,
		        p.id, p.name,
		        d.id, d.name,
		        dp.id, prod.name || COALESCE(' ' || pv.version, ''),
		        prod.id, prod.name,
		        a.id, a.name, a.tier,
		        ae.id, ae.first_name || ' ' || ae.last_name,
		        pc.id, pc.number,
		        rc.id, rc.number
		 FROM cases c
		 JOIN users u ON u.id = c.created_by
		 JOIN projects p ON p.id = c.project_id
		 JOIN accounts a ON a.id = p.account_id
		 JOIN deployments d ON d.id = c.deployment_id
		 JOIN deployed_products dp ON dp.id = c.deployed_product_id
		 JOIN products prod ON prod.id = dp.product_id
		 LEFT JOIN product_versions pv ON pv.id = dp.product_version_id
		 LEFT JOIN users ae ON ae.id = c.assigned_engineer
		 LEFT JOIN cases pc ON pc.id = c.parent_case_id
		 LEFT JOIN cases rc ON rc.id = c.related_case_id
		 WHERE c.id = $1`, id,
	).Scan(
		&cv.ID, &cv.Number, &cv.InternalID,
		&cv.Subject, &cv.Description, &cv.Severity, &cv.IssueType, &cv.State, &workState,
		&cv.CreatedOn, &cv.UpdatedOn, &cv.ClosedOn,
		&cv.CreatedByDetails.ID, &cv.CreatedByDetails.Name, &cv.CreatedByDetails.UserID, &cv.CreatedByDetails.Email,
		&cv.ProjectDetails.ID, &cv.ProjectDetails.Name,
		&cv.DeploymentDetails.ID, &cv.DeploymentDetails.Name,
		&cv.DeployedProductDetails.ID, &cv.DeployedProductDetails.DisplayName,
		&cv.ProductDetails.ID, &cv.ProductDetails.Name,
		&accountID, &accountName, &accountTier,
		&aeID, &aeName,
		&pcID, &pcNum,
		&rcID, &rcNum,
	)
	if errors.Is(err, pgx.ErrNoRows) {
		return domain.CaseView{}, &apierror.NotFoundError{Msg: "case not found"}
	}
	if err != nil {
		return domain.CaseView{}, fmt.Errorf("get case by id: %w", err)
	}
	cv.AccountDetails = &domain.AccountRef{ID: accountID, Name: accountName, Type: accountTier}
	if workState != nil {
		ws := domain.CaseWorkState(*workState)
		cv.WorkState = &ws
	}
	if aeID != nil {
		cv.AssignedEngineer = &domain.AssignedEngineerRef{ID: *aeID, Name: *aeName}
	}
	if pcID != nil {
		cv.ParentCase = &domain.CaseNumberRef{ID: *pcID, Number: *pcNum}
	}
	if rcID != nil {
		cv.RelatedCase = &domain.CaseNumberRef{ID: *rcID, Number: *rcNum}
	}
	return cv, nil
}

// CreateCaseComment implements CaseRepository.
func (r *caseRepo) CreateCaseComment(ctx context.Context, req domain.CreateCaseCommentRequest) (domain.CaseComment, error) {
	const query = `
		INSERT INTO case_comments (case_id, type, content, created_by)
		VALUES ($1, $2::comment_type_enum, $3, $4)
		RETURNING id, case_id, type, content, created_by, created_at`

	var c domain.CaseComment
	err := r.db.QueryRow(ctx, query,
		req.CaseID, string(req.TypeKey), req.Content, req.CreatedBy,
	).Scan(&c.ID, &c.CaseID, &c.Type, &c.Content, &c.CreatedBy, &c.CreatedOn)
	if err != nil {
		if pgErr := (*pgconn.PgError)(nil); errors.As(err, &pgErr) {
			switch pgErr.Code {
			case "23503": // foreign_key_violation — case_id or created_by does not exist
				return domain.CaseComment{}, &apierror.ValidationError{Msg: "one or more referenced IDs do not exist: " + pgErr.Detail}
			case "P0001": // raise_exception from user-type triggers
				return domain.CaseComment{}, &apierror.ValidationError{Msg: pgErr.Message}
			}
		}
		return domain.CaseComment{}, fmt.Errorf("create case comment: %w", err)
	}
	return c, nil
}

// SearchCaseComments implements CaseRepository.
func (r *caseRepo) SearchCaseComments(ctx context.Context, req domain.SearchCaseCommentsRequest) ([]domain.CaseComment, int, error) {
	args := []any{req.CaseID}
	typeFilter := ""
	if req.Filters != nil && req.Filters.TypeKey != nil {
		args = append(args, string(*req.Filters.TypeKey))
		typeFilter = fmt.Sprintf(" AND cc.type = $%d::comment_type_enum", len(args))
	}

	countQuery := `SELECT COUNT(*) FROM case_comments cc WHERE cc.case_id = $1` + typeFilter
	dataQuery := fmt.Sprintf(`
		SELECT cc.id, cc.case_id, cc.type, cc.content,
		       u.id, u.first_name, u.last_name,
		       TRIM(u.first_name || ' ' || u.last_name) AS full_name,
		       cc.created_at
		FROM case_comments cc
		JOIN users u ON u.id = cc.created_by
		WHERE cc.case_id = $1%s
		ORDER BY cc.created_at DESC, cc.id
		LIMIT $%d OFFSET $%d`, typeFilter, len(args)+1, len(args)+2)

	dataArgs := append(args, req.Pagination.Limit, req.Pagination.Offset)

	var total int
	var comments []domain.CaseComment

	eg, egCtx := errgroup.WithContext(ctx)

	eg.Go(func() error {
		if err := r.db.QueryRow(egCtx, countQuery, args...).Scan(&total); err != nil {
			return fmt.Errorf("count case comments: %w", err)
		}
		return nil
	})

	eg.Go(func() error {
		rows, err := r.db.Query(egCtx, dataQuery, dataArgs...)
		if err != nil {
			return fmt.Errorf("query case comments: %w", err)
		}
		defer rows.Close()

		result := make([]domain.CaseComment, 0, req.Pagination.Limit)
		for rows.Next() {
			var c domain.CaseComment
			if err := rows.Scan(
				&c.ID, &c.CaseID, &c.Type, &c.Content,
				&c.CreatedBy.ID, &c.CreatedBy.FirstName, &c.CreatedBy.LastName,
				&c.CreatedBy.FullName, &c.CreatedOn,
			); err != nil {
				return fmt.Errorf("scan case comment: %w", err)
			}
			result = append(result, c)
		}
		if err := rows.Err(); err != nil {
			return fmt.Errorf("iterate case comments: %w", err)
		}
		comments = result
		return nil
	})

	if err := eg.Wait(); err != nil {
		return nil, 0, err
	}

	return comments, total, nil
}

// UpdateCase implements CaseRepository.
func (r *caseRepo) UpdateCase(ctx context.Context, req domain.UpdateCaseRequest) (domain.Case, error) {
	state := ""
	if req.StateKey != nil {
		state = string(*req.StateKey)
	}
	severity := ""
	if req.SeverityKey != nil {
		severity = string(*req.SeverityKey)
	}
	workState := ""
	if req.WorkStateKey != nil {
		workState = string(*req.WorkStateKey)
	}
	const query = `
		UPDATE cases
		SET state      = CASE WHEN $2 <> '' THEN $2::case_state_enum ELSE state END,
		    severity   = CASE WHEN $3 <> '' THEN $3::case_severity_enum ELSE severity END,
		    work_state = CASE WHEN $4 <> '' THEN $4::case_work_state_enum ELSE work_state END,
		    updated_at = NOW(),
		    closed_at  = CASE WHEN $2 = 'closed' THEN NOW() WHEN $2 <> '' AND $2 <> 'closed' THEN NULL ELSE closed_at END
		WHERE id = $1
		RETURNING id, number, internal_id, created_by, project_id, deployment_id, deployed_product_id,
		          subject, description, severity, issue_type, state, work_state, created_at, updated_at, closed_at`

	var c domain.Case
	var workStateRaw *string
	err := r.db.QueryRow(ctx, query, req.ID, state, severity, workState).Scan(
		&c.ID, &c.Number, &c.InternalID, &c.CreatedBy,
		&c.ProjectID, &c.DeploymentID, &c.DeployedProductID,
		&c.Subject, &c.Description, &c.Severity, &c.IssueType, &c.State, &workStateRaw,
		&c.CreatedOn, &c.UpdatedOn, &c.ClosedOn,
	)
	if errors.Is(err, pgx.ErrNoRows) {
		return domain.Case{}, &apierror.NotFoundError{Msg: "case not found"}
	}
	if err != nil {
		return domain.Case{}, fmt.Errorf("update case: %w", err)
	}
	if workStateRaw != nil {
		ws := domain.CaseWorkState(*workStateRaw)
		c.WorkState = &ws
	}
	return c, nil
}

// pgSortColMap maps domain CaseSortField values to Postgres column expressions.
var pgSortColMap = map[domain.CaseSortField]string{
	domain.CaseSortFieldCreatedOn: "c.created_at",
	domain.CaseSortFieldUpdatedOn: "c.updated_at",
	domain.CaseSortFieldSeverity:  "c.severity",
	domain.CaseSortFieldState:     "c.state",
}

// SearchCases implements CaseRepository.
func (r *caseRepo) SearchCases(ctx context.Context, req domain.SearchCasesRequest) ([]domain.SearchCaseView, int, error) {
	filterArgs := []any{}
	argIdx := 1

	where := "WHERE 1=1"

	if len(req.Filters.TypeKeys) > 0 {
		where += fmt.Sprintf(" AND c.type = ANY($%d::case_type_enum[])", argIdx)
		filterArgs = append(filterArgs, req.Filters.TypeKeys)
		argIdx++
	}

	if len(req.Filters.ProjectIDs) > 0 {
		where += fmt.Sprintf(" AND c.project_id = ANY($%d::uuid[])", argIdx)
		filterArgs = append(filterArgs, req.Filters.ProjectIDs)
		argIdx++
	}

	if len(req.Filters.DeploymentIDs) > 0 {
		where += fmt.Sprintf(" AND c.deployment_id = ANY($%d::uuid[])", argIdx)
		filterArgs = append(filterArgs, req.Filters.DeploymentIDs)
		argIdx++
	}

	if len(req.Filters.StateKeys) > 0 {
		stateStrings := make([]string, len(req.Filters.StateKeys))
		for i, s := range req.Filters.StateKeys {
			stateStrings[i] = string(s)
		}
		where += fmt.Sprintf(" AND c.state = ANY($%d::case_state_enum[])", argIdx)
		filterArgs = append(filterArgs, stateStrings)
		argIdx++
	}

	if len(req.Filters.SeverityKeys) > 0 {
		severityStrings := make([]string, len(req.Filters.SeverityKeys))
		for i, s := range req.Filters.SeverityKeys {
			severityStrings[i] = string(s)
		}
		where += fmt.Sprintf(" AND c.severity = ANY($%d::case_severity_enum[])", argIdx)
		filterArgs = append(filterArgs, severityStrings)
		argIdx++
	}

	if len(req.Filters.IssueTypeKeys) > 0 {
		issueTypeStrings := make([]string, len(req.Filters.IssueTypeKeys))
		for i, it := range req.Filters.IssueTypeKeys {
			issueTypeStrings[i] = string(it)
		}
		where += fmt.Sprintf(" AND c.issue_type = ANY($%d::case_issue_type_enum[])", argIdx)
		filterArgs = append(filterArgs, issueTypeStrings)
		argIdx++
	}

	if len(req.Filters.EngagementTypeKeys) > 0 {
		engTypeStrings := make([]string, len(req.Filters.EngagementTypeKeys))
		for i, et := range req.Filters.EngagementTypeKeys {
			engTypeStrings[i] = string(et)
		}
		where += fmt.Sprintf(" AND c.engagement_type = ANY($%d::engagement_type_enum[])", argIdx)
		filterArgs = append(filterArgs, engTypeStrings)
		argIdx++
	}

	if len(req.Filters.CreatedBy) > 0 {
		where += fmt.Sprintf(" AND u.email = ANY($%d)", argIdx)
		filterArgs = append(filterArgs, req.Filters.CreatedBy)
		argIdx++
	}

	if req.Filters.ClosedStartDate != nil {
		where += fmt.Sprintf(" AND c.closed_at >= $%d", argIdx)
		filterArgs = append(filterArgs, req.Filters.ClosedStartDate)
		argIdx++
	}
	if req.Filters.ClosedEndDate != nil {
		where += fmt.Sprintf(" AND c.closed_at <= $%d", argIdx)
		filterArgs = append(filterArgs, req.Filters.ClosedEndDate)
		argIdx++
	}
	if req.Filters.StartCreatedDate != nil {
		where += fmt.Sprintf(" AND c.created_at >= $%d", argIdx)
		filterArgs = append(filterArgs, req.Filters.StartCreatedDate)
		argIdx++
	}
	if req.Filters.EndCreatedDate != nil {
		where += fmt.Sprintf(" AND c.created_at <= $%d", argIdx)
		filterArgs = append(filterArgs, req.Filters.EndCreatedDate)
		argIdx++
	}
	if req.Filters.StartUpdatedDate != nil {
		where += fmt.Sprintf(" AND c.updated_at >= $%d", argIdx)
		filterArgs = append(filterArgs, req.Filters.StartUpdatedDate)
		argIdx++
	}
	if req.Filters.EndUpdatedDate != nil {
		where += fmt.Sprintf(" AND c.updated_at <= $%d", argIdx)
		filterArgs = append(filterArgs, req.Filters.EndUpdatedDate)
		argIdx++
	}

	if req.Filters.SearchQuery != "" {
		escaped := strings.NewReplacer(`\`, `\\`, `%`, `\%`, `_`, `\_`).Replace(req.Filters.SearchQuery)
		pattern := "%" + escaped + "%"
		where += fmt.Sprintf(` AND (c.subject ILIKE $%d ESCAPE '\' OR c.number ILIKE $%d ESCAPE '\' OR c.internal_id ILIKE $%d ESCAPE '\')`, argIdx, argIdx, argIdx)
		filterArgs = append(filterArgs, pattern)
		argIdx++
	}

	sortCol := pgSortColMap[req.SortBy.Field]
	sortDir := string(req.SortBy.Order)

	joins := `JOIN users u ON u.id = c.created_by
		 JOIN projects p ON p.id = c.project_id
		 JOIN deployments d ON d.id = c.deployment_id
		 JOIN deployed_products dp ON dp.id = c.deployed_product_id
		 JOIN products prod ON prod.id = dp.product_id
		 LEFT JOIN product_versions pv ON pv.id = dp.product_version_id
		 LEFT JOIN users ae ON ae.id = c.assigned_engineer
		 LEFT JOIN cases pc ON pc.id = c.parent_case_id
		 LEFT JOIN cases rc ON rc.id = c.related_case_id`

	countQuery := "SELECT COUNT(*) FROM cases c " + joins + " " + where

	dataQuery := fmt.Sprintf(
		`SELECT c.id, c.number, c.internal_id,
		        c.type, c.subject, c.description, c.severity, c.issue_type, c.state,
		        c.engagement_type, c.work_state, c.created_at,
		        u.email,
		        p.id, p.name,
		        d.id, d.name,
		        dp.id, prod.name || COALESCE(' ' || pv.version, ''),
		        prod.id, prod.name,
		        ae.id, ae.first_name || ' ' || ae.last_name,
		        pc.id, pc.number,
		        rc.id, rc.number
		 FROM cases c %s %s
		 ORDER BY %s %s NULLS LAST, c.id
		 LIMIT $%d OFFSET $%d`,
		joins, where, sortCol, sortDir, argIdx, argIdx+1,
	)
	dataArgs := append(append([]any{}, filterArgs...), req.Pagination.Limit, req.Pagination.Offset)

	var total int
	var cases []domain.SearchCaseView

	eg, egCtx := errgroup.WithContext(ctx)

	eg.Go(func() error {
		if err := r.db.QueryRow(egCtx, countQuery, filterArgs...).Scan(&total); err != nil {
			return fmt.Errorf("count cases: %w", err)
		}
		return nil
	})

	eg.Go(func() error {
		rows, err := r.db.Query(egCtx, dataQuery, dataArgs...)
		if err != nil {
			return fmt.Errorf("query cases: %w", err)
		}
		defer rows.Close()

		result := make([]domain.SearchCaseView, 0, req.Pagination.Limit)
		for rows.Next() {
			var cv domain.SearchCaseView
			var caseType, subject, description string
			var severity, issueType, engagementType, workState *string
			var createdAt time.Time
			var aeID, aeName *string
			var pcID, pcNumber *string
			var rcID, rcNumber *string
			var prodID, prodName string
			if err := rows.Scan(
				&cv.ID, &cv.Number, &cv.InternalID,
				&caseType, &subject, &description, &severity, &issueType, &cv.State,
				&engagementType, &workState, &createdAt,
				&cv.CreatedBy,
				&cv.Project.ID, &cv.Project.Name,
				&cv.Deployment.ID, &cv.Deployment.Name,
				&cv.DeployedProduct.ID, &cv.DeployedProduct.Name,
				&prodID, &prodName,
				&aeID, &aeName,
				&pcID, &pcNumber,
				&rcID, &rcNumber,
			); err != nil {
				return fmt.Errorf("scan case: %w", err)
			}
			cv.CaseType = caseType
			cv.Title = &subject
			cv.Description = &description
			cv.Severity = severity
			cv.IssueType = issueType
			cv.EngagementType = engagementType
			cv.WorkState = workState
			cv.CreatedOn = createdAt.UTC().Format(time.RFC3339)
			cv.Product = &domain.EntityRef{ID: prodID, Name: prodName}
			if aeID != nil {
				cv.AssignedEngineer = &domain.AssignedEngineerRef{ID: *aeID, Name: *aeName}
			}
			if pcID != nil {
				cv.ParentCase = &domain.EntityRef{ID: *pcID, Name: *pcNumber}
			}
			if rcID != nil {
				cv.RelatedCase = &domain.EntityRef{ID: *rcID, Name: *rcNumber}
			}
			result = append(result, cv)
		}
		if err := rows.Err(); err != nil {
			return fmt.Errorf("iterate cases: %w", err)
		}
		cases = result
		return nil
	})

	if err := eg.Wait(); err != nil {
		return nil, 0, err
	}

	return cases, total, nil
}
